import assert from "node:assert/strict";
import { execFileSync } from "node:child_process";
import { cp, mkdir, mkdtemp, readFile, rm, writeFile } from "node:fs/promises";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { test } from "node:test";

test("release commit includes the synchronized Cargo lockfile", async (t) => {
  const projectDir = await mkdtemp(join(tmpdir(), "db-connect-release-"));
  const remoteDir = await mkdtemp(join(tmpdir(), "db-connect-release-origin-"));
  t.after(async () => {
    await rm(projectDir, { recursive: true, force: true });
    await rm(remoteDir, { recursive: true, force: true });
  });

  await seedReleaseProject(projectDir);
  await cp(join(import.meta.dirname, "release.mjs"), join(projectDir, "scripts", "release.mjs"));
  await cp(
    join(import.meta.dirname, "release-utils.mjs"),
    join(projectDir, "scripts", "release-utils.mjs")
  );

  git(["init", "--initial-branch=main"], projectDir);
  git(["config", "user.name", "Release Test"], projectDir);
  git(["config", "user.email", "release-test@example.com"], projectDir);
  execFileSync("git", ["init", "--bare", "--initial-branch=main", remoteDir], {
    encoding: "utf8",
  });
  git(["remote", "add", "origin", remoteDir], projectDir);
  git(["add", "."], projectDir);
  git(["commit", "-m", "chore: 初始化发布测试项目"], projectDir);

  execFileSync(process.execPath, ["scripts/release.mjs"], {
    cwd: projectDir,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
  });

  const committedLockfile = git(["show", "HEAD:src-tauri/Cargo.lock"], projectDir);
  assert.match(committedLockfile, /name = "db-connect"\nversion = "1\.0\.1"/);

  const committedFiles = git(["show", "--name-only", "--format=", "HEAD"], projectDir)
    .trim()
    .split("\n");
  assert.ok(committedFiles.includes("src-tauri/Cargo.lock"));
  assert.equal(git(["status", "--porcelain"], projectDir), "");
});

async function seedReleaseProject(projectDir) {
  await mkdir(join(projectDir, "scripts"), { recursive: true });
  await mkdir(join(projectDir, "src"), { recursive: true });
  await mkdir(join(projectDir, "src-tauri", "src"), { recursive: true });

  await writeJson(join(projectDir, "package.json"), {
    name: "db-connect",
    version: "1.0.0",
  });
  await writeJson(join(projectDir, "package-lock.json"), {
    name: "db-connect",
    version: "1.0.0",
    lockfileVersion: 3,
    requires: true,
    packages: {
      "": {
        name: "db-connect",
        version: "1.0.0",
      },
    },
  });
  await writeJson(join(projectDir, "src-tauri", "tauri.conf.json"), {
    version: "1.0.0",
  });
  await writeFile(
    join(projectDir, "src-tauri", "Cargo.toml"),
    `[package]
name = "db-connect"
version = "1.0.0"
edition = "2021"
`
  );
  await writeFile(
    join(projectDir, "src-tauri", "Cargo.lock"),
    `# This file is automatically @generated by Cargo.
version = 4

[[package]]
name = "db-connect"
version = "1.0.0"
`
  );
  await writeFile(join(projectDir, "src-tauri", "src", "lib.rs"), "");
  await writeFile(
    join(projectDir, "src", "appVersion.ts"),
    `declare const __APP_VERSION__: string | undefined;

export function getAppVersion(): string {
  return typeof __APP_VERSION__ === "string" && __APP_VERSION__.length > 0
    ? __APP_VERSION__
    : "1.0.0";
}
`
  );

  const lockfile = await readFile(join(projectDir, "src-tauri", "Cargo.lock"), "utf8");
  assert.match(lockfile, /version = "1\.0\.0"/);
}

async function writeJson(path, value) {
  await writeFile(path, `${JSON.stringify(value, null, 2)}\n`);
}

function git(args, cwd) {
  return execFileSync("git", args, {
    cwd,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
  });
}

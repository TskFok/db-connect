  // 表格实际渲染高度：优先用用户拖拽设定的值（并按 slot 最大值 clamp），否则用自动高度
  const effectiveTableHeight =
    userTableHeight != null
      ? Math.min(userTableHeight, maxUserTableHeightRef.current)
      : autoTableHeight;

  // 拖拽 resize bar：mousedown 后跟踪 document 级 mousemove，松开时落地 localStorage

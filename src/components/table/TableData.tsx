/** 排序指示器：使用 Unicode 字符替代图标组件，减少 DOM 与 React 开销 */
function SortIndicator({
  active,
  direction,
  priority,
  ariaLabel,
  title,
  onClick,
}: {
  active: boolean;
  direction?: "ASC" | "DESC";
  /** 从 1 开始；多列排序时显示优先级序号 */
  priority?: number;
  ariaLabel?: string;
  title?: string;
  onClick: (e: ReactMouseEvent<HTMLSpanElement>) => void;
}) {
  const upColor =
    active && direction === "ASC" ? "#1677ff" : "var(--text-muted)";
  const downColor =
    active && direction === "DESC" ? "#1677ff" : "var(--text-muted)";
  return (
    <span
      role="button"
      aria-label={ariaLabel ?? "点击排序"}
      title={title ?? "排序"}
      onClick={(e) => {
        e.stopPropagation();
        onClick(e);
      }}
      style={{
        display: "inline-flex",
        flexDirection: "column",
        alignItems: "center",
        justifyContent: "center",
        cursor: "pointer",
        padding: "0 2px",
        marginLeft: 2,
        lineHeight: 1,
        flexShrink: 0,
        fontSize: 9,
        position: "relative",
      }}
    >
      {priority != null && priority > 0 ? (
        <span
          style={{
            position: "absolute",
            right: -1,
            top: -6,
            fontSize: 8,
            lineHeight: 1,
            color: "var(--text-muted)",
            fontWeight: 600,
          }}
        >
          {priority}
        </span>
      ) : null}
      <span style={{ color: upColor }}>▲</span>
      <span style={{ color: downColor, marginTop: -2 }}>▼</span>
    </span>
  );
}

